package controller;

public class Piloto {
	private int carro;
	private int equipe;
	private double tempo;
	
	public Piloto(){
		this.carro = carro;
		this.equipe = equipe;
		this.tempo = tempo;
	}
	public int getCarro(){
		return this.carro;
	}
	public int getEquipe (){
		return equipe;
	}
	public double getTempo(){
		return this.tempo;
	}
	public void setCarro(int carro){
		this.carro = carro;
	}
	public void setEquipe(int equipe){
		this.equipe = equipe;
	}
	public void setTempo(double tempo){
		this.tempo = tempo;
	}
	
	
	
}
